function exercise(input) {
  return {
    ...input,
    tags: uniqueStrings([input.source, input.chapter, ...(input.concepts || [])]),
  };
}

function localExerciseLibraryForCourse(courseModel = {}, options = {}) {
  const profileId = courseModel.course?.focus_profile?.id || "material_mechanics";
  const base = LIBRARIES[profileId] || MATERIAL_EXERCISES;
  const conceptNames = new Set((courseModel.concepts || []).map((concept) => concept.name).filter(Boolean));
  const chapterTitles = new Set((courseModel.chapters || []).map((chapter) => compactKey(chapter.title)).filter(Boolean));
  const selectedDocs = (courseModel.documents || []).flatMap((doc) => doc.source_refs || []);
  const fallbackRefs = selectedDocs.length ? selectedDocs : [{ document_id: "local_exercise_library", file_name: "本地题库", unit_label: "本地资源", unit_index: 0 }];
  const scored = base
    .map((item, index) => {
      const conceptHits = (item.concepts || []).filter((concept) => conceptNames.has(concept));
      const exerciseChapterKey = compactKey(item.chapter);
      const chapterHit = [...chapterTitles].some((title) => title && (exerciseChapterKey.includes(title) || title.includes(exerciseChapterKey)));
      const sourceRefs = sourceRefsForExercise(item, courseModel, fallbackRefs);
      return {
        ...item,
        score: conceptHits.length * 28 + (chapterHit ? 20 : 0) + typePriority(item.type) - index * 0.01,
        matched_concepts: conceptHits,
        source_refs: sourceRefs,
      };
    })
    .sort((a, b) => b.score - a.score);
  const minCount = Number(options.minCount || 10);
  const matched = scored.filter((item) => item.score >= 18);
  return (matched.length >= Math.min(minCount, scored.length) ? matched : scored).slice(0, Number(options.limit || 24));
}

function sourceRefsForExercise(exerciseItem, courseModel, fallbackRefs) {
  const refs = [];
  const relatedTexts = [exerciseItem.chapter, ...(exerciseItem.concepts || [])].map(compactKey);
  for (const chapter of courseModel.chapters || []) {
    const chapterKey = compactKey(chapter.title);
    if (relatedTexts.some((text) => text && (chapterKey.includes(text) || text.includes(chapterKey)))) {
      refs.push(...(chapter.source_refs || []));
    }
  }
  for (const concept of courseModel.concepts || []) {
    if ((exerciseItem.concepts || []).includes(concept.name)) refs.push(...(concept.source_refs || []));
  }
  const normalized = uniqueByRef(refs).slice(0, 2);
  const basis = normalized.length ? normalized : fallbackRefs.slice(0, 1);
  return basis.map((ref) => ({
    ...ref,
    locator_type: ref.locator_type || "local_exercise_library",
    locator_label: exerciseItem.source,
    anchor_label: exerciseItem.source,
    excerpt: `${exerciseItem.source}：${clamp(exerciseItem.stem, 140)}`,
    confidence: ref.confidence || "medium",
    locator_confidence: ref.locator_confidence || ref.confidence || "medium",
  }));
}

function uniqueByRef(refs = []) {
  const seen = new Set();
  const out = [];
  for (const ref of refs) {
    const key = `${ref.document_id}:${ref.unit_index}:${ref.unit_label}`;
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(ref);
  }
  return out;
}

function typePriority(type) {
  return type === "exam_practice" ? 18 : type === "textbook_exercise" ? 16 : 10;
}

function compactKey(value) {
  return String(value || "").replace(/\s+/g, "").toLowerCase();
}

function buildExerciseQuestion(exerciseItem, variantIndex = 0) {
  const questionText =
    variantIndex > 0
      ? `变式拓展：基于“${clamp(exerciseItem.stem, 120)}”，改变一个已知条件或边界条件，重新写出解题入口、列式步骤和校核点。`
      : `${exerciseItem.source}：${exerciseItem.stem}`;
  const answer =
    variantIndex > 0
      ? "变式题仍应先判断研究对象和适用条件，再重列方程。变化条件若影响内力、几何或材料参数，必须重新计算；无关条件要说明不采用。"
      : exerciseItem.answer;
  return {
    seed: ["local_exercise_library", LIBRARY_VERSION, exerciseItem.id, variantIndex],
    question_type: variantIndex > 0 ? "variant" : exerciseItem.type,
    difficulty: variantIndex > 0 && exerciseItem.difficulty !== "hard" ? "hard" : exerciseItem.difficulty,
    related_concepts: exerciseItem.concepts || [],
    source_refs: exerciseItem.source_refs || [],
    question_text: questionText,
    answer,
    step_by_step_solution: variantIndex > 0
      ? ["对照原题列出改变条件。", "判断哪些方程或校核条件会改变。", "重建解题流程。", "说明单位、正负号和适用条件。"]
      : exerciseItem.steps,
    common_mistakes: exerciseItem.mistakes,
    grading_rubric: exerciseItem.rubric,
    estimated_time: Number(exerciseItem.time || 8) + (variantIndex > 0 ? 2 : 0),
    tags: uniqueStrings([...(exerciseItem.tags || []), variantIndex > 0 ? "变式拓展" : "本地题库"]),
  };
}

